binary = 0b11010  # The allowed digits are prefix is (0b) 0 and 1
print(binary)

octal = 0O147654  # The allowed digits are prefix is(0O) 0 - 7.
print(octal)

# The allowed numbers are prefix is(0X) 0 - 15(a,b,c,d,e,f)
hexadecimal = 0xFabcde
print(hexadecimal)

# Converting all number formats into binary

print(bin(374658512))
print(bin(0O7416320))
print(bin(0xabcef))
print(bin(0x123456789))

# Converting all number formats into Octal

print(oct(164))
print(oct(0b110101010101111))
print(oct(0o14755))
print(oct(0x1245facbd))

# Converting all number formats into Hexadecimal

print(hex(456123))
print(hex(0b1101010))
print(hex(0O771456))
print(hex(0x123456789))
